const filePath = path.resolve(__dirname, '..', 'static', 'personality-types.json')
const raw = JSON.parse(fs.readFileSync(filePath, 'utf-8'))

const enhanced = raw.map(item => {
  const t = item.type
  const desc = item.description || ''
  const portrait = desc.length > 500 ? desc.substring(0, 500) + '...' : desc
  const careers = careerAdvice[t] || []
  const sw = strengthsWeaknesses[t] || { strengths: [], weaknesses: [] }

  return {
    ...item,
    portrait,
    career: careers,
    strengths: sw.strengths,
    weaknesses: sw.weaknesses
  }
})

fs.writeFileSync(filePath, JSON.stringify(enhanced, null, 2), 'utf-8')
